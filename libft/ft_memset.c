#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	unsigned char	*i;

	i = (unsigned char *)str;
	while (n > 0)
	{
		*i++ = (unsigned char)c;
		n--;
	}
	return (str);
}
