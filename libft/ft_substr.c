#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*ret;

	if (len > ft_strlen(s))
		len = ft_strlen(s);
	if (start >= ft_strlen(s))
		return (ft_strdup(""));
	if (ft_strlen(s + start) < len)
		len = ft_strlen(s + start);
	ret = ((char *)malloc(sizeof(char) * (len + 1)));
	if (!ret || !s)
		return (NULL);
	ft_strlcpy(ret, s + start, len + 1);
	return (ret);
}
