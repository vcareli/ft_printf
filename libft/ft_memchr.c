#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*search;
	unsigned char	c2;
	size_t			i;

	search = (unsigned char *)s;
	c2 = (unsigned char)c;
	i = 0;
	while (i < n)
	{
		if (c2 == search[i])
			return (search + i);
		i++;
	}
	return (NULL);
}
